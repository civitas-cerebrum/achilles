#!/usr/bin/env node
// build-validator.mjs — generates hooks/lib/validator.bundle.mjs, a
// dependency-free single-file validator with every schema inlined.
// Spec: skills/element-interactions/references/harness-hooks.md
//
// Subcommand contract of the emitted bundle:
//   validate <role|onboarding-status> <data-file>
//     exit 0 valid; exit 1 invalid (stderr lines prefixed SCHEMA_FAIL: or
//     PARSE_FAIL:); exit 2 usage / unknown schema id.
//   replace <target-file> <old-file> <new-file> [--all]
//     stdout = file content with literal replacement applied.
//     exit 0 ok; exit 3 old-string not found; exit 4 old-string matched
//     more than once without --all; exit 2 usage or unreadable input files
//     (stderr line prefixed REPLACE_FAIL:).
//   tojson <file>
//     reads <file>, parses it as YAML (a superset of JSON), and writes the
//     JSON.stringify of the result to stdout. exit 0 ok; exit 1 on parse
//     failure (stderr line prefixed PARSE_FAIL:); exit 2 usage / unreadable
//     input. Consumed by P1's workflow-reviewer-attestation-gate.sh.

import { readdirSync, writeFileSync, rmSync } from 'node:fs';
import { basename } from 'node:path';
import { build } from 'esbuild';

const RETURNS_DIR = 'schemas/subagent-returns';
const roleFiles = readdirSync(RETURNS_DIR).filter((f) => f.endsWith('.schema.json'));

const imports = [];
const entries = [];
roleFiles.forEach((f, i) => {
  imports.push(`import s${i} from '../${RETURNS_DIR}/${f}';`);
  entries.push(`  ${JSON.stringify(basename(f, '.schema.json'))}: s${i},`);
});
imports.push(`import sOnboarding from '../schemas/onboarding-status.schema.json';`);
entries.push(`  'onboarding-status': sOnboarding,`);
imports.push(`import sPerfOnboarding from '../schemas/perf-onboarding-status.schema.json';`);
entries.push(`  'perf-onboarding-status': sPerfOnboarding,`);
imports.push(`import sRunSummary from '../schemas/run-summary.schema.json';`);
entries.push(`  'run-summary': sRunSummary,`);
imports.push(`import sPerfSummary from '../schemas/perf-summary.schema.json';`);
entries.push(`  'perf-summary': sPerfSummary,`);

const entrySource = `// GENERATED by scripts/build-validator.mjs — do not edit.
import { readFileSync } from 'node:fs';
import { parse as parseYaml } from 'yaml';
import Ajv from 'ajv/dist/2020.js';
import addFormats from 'ajv-formats';
${imports.join('\n')}

const SCHEMAS = {
${entries.join('\n')}
};

function makeAjv() {
  const AjvCtor = Ajv.default || Ajv;
  const ajv = new AjvCtor({ strict: true, allErrors: true, allowUnionTypes: true, strictSchema: false });
  (addFormats.default || addFormats)(ajv);
  if (SCHEMAS['handover']) ajv.addSchema(SCHEMAS['handover']);
  return ajv;
}

function cmdValidate(id, dataFile) {
  const schema = SCHEMAS[id];
  if (!schema) { console.error('No schema for id: ' + id); process.exit(2); }
  let data;
  try { data = parseYaml(readFileSync(dataFile, 'utf8')); }
  catch (e) { console.error('PARSE_FAIL: ' + e.message); process.exit(1); }
  const validate = makeAjv().compile(schema);
  if (!validate(data)) {
    for (const err of validate.errors ?? []) {
      console.error('SCHEMA_FAIL: ' + (err.instancePath || '/') + ' ' + err.message);
    }
    process.exit(1);
  }
  process.exit(0);
}

function cmdReplace(targetFile, oldFile, newFile, all) {
  let content, oldStr, newStr;
  try {
    content = readFileSync(targetFile, 'utf8');
    oldStr  = readFileSync(oldFile,   'utf8');
    newStr  = readFileSync(newFile,   'utf8');
  } catch (e) { console.error('REPLACE_FAIL: ' + e.message); process.exit(2); }
  if (oldStr.length === 0) { console.error('REPLACE_FAIL: empty old-string'); process.exit(2); }
  const count = content.split(oldStr).length - 1;
  if (count === 0) { console.error('REPLACE_FAIL: old-string not found'); process.exit(3); }
  if (count > 1 && !all) { console.error('REPLACE_FAIL: old-string matched ' + count + ' times (not unique)'); process.exit(4); }
  const out = all
    ? content.split(oldStr).join(newStr)
    : content.replace(oldStr, () => newStr); // function form: no $-pattern expansion
  process.stdout.write(out);
  process.exit(0);
}

function cmdTojson(file) {
  let raw;
  try { raw = readFileSync(file, 'utf8'); }
  catch (e) { console.error('PARSE_FAIL: ' + e.message); process.exit(2); }
  let data;
  try { data = parseYaml(raw); }
  catch (e) { console.error('PARSE_FAIL: ' + e.message); process.exit(1); }
  process.stdout.write(JSON.stringify(data));
  process.exit(0);
}

const [, , cmd, ...args] = process.argv;
if (cmd === 'validate' && args.length === 2) cmdValidate(args[0], args[1]);
else if (cmd === 'replace' && (args.length === 3 || (args.length === 4 && args[3] === '--all'))) cmdReplace(args[0], args[1], args[2], args.length === 4);
else if (cmd === 'tojson' && args.length === 1) cmdTojson(args[0]);
else { console.error('Usage: validator.bundle.mjs validate <id> <data-file> | replace <target> <old-file> <new-file> [--all] | tojson <file>'); process.exit(2); }
`;

const GEN_ENTRY = 'scripts/.validator-entry.gen.mjs';
writeFileSync(GEN_ENTRY, entrySource);
rmSync('hooks/lib/validator.bundle.mjs', { force: true });
try {
  await build({
    entryPoints: [GEN_ENTRY],
    bundle: true,
    platform: 'node',
    format: 'esm',
    outfile: 'hooks/lib/validator.bundle.mjs',
    banner: { js: '#!/usr/bin/env node\nimport { createRequire } from "module"; const require = createRequire(import.meta.url);' },
    legalComments: 'none',
    logLevel: 'warning',
  });
  console.log('✓ hooks/lib/validator.bundle.mjs');
} finally {
  rmSync(GEN_ENTRY, { force: true });
}
